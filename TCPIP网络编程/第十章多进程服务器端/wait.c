#include <stdio.h>
#include <stdlib.h>
#include <unistd.h>
#include <sys/wait.h>

int main(int argc, char* argv[]) {
    int status;
    pid_t pid = fork();
    if (pid == 0) {
        return 3;
    } else {
        printf("Child PID: %d\n", pid);
        pid = fork();
        if (pid == 0) {
            exit(7);
        } else {
            printf("Child PID: %d\n", pid); // 修正此处的拼写错误
            wait(&status);
            if (WIFEXITED(status)) {   // 修正此处的拼写错误
                printf("Child send one: %d\n", WEXITSTATUS(status)); // 修正末尾的句点为双引号
            }
            wait(&status);
            if (WIFEXITED(status)) {   // 修正此处的拼写错误
                printf("Child send two: %d\n", WEXITSTATUS(status)); // 修正末尾的句点为双引号
            }
            sleep(30);
        }
    }
    return 0;
}

